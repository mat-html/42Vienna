#include <unistd.h>

void	ft_putchar(int col, int row, int x, int y)
{
	if (row == 1 || row == y)
	{
		if (col == 1 || col == x)
		{
			write(1, "o", 1);
		}
		else
		{
			write(1, "-", 1);
		}
	}
	else
	{
		if (col == 1 || col == x)
		{
			write(1, "|", 1);
		}
		else
		{
			write(1, " ", 1);
		}
	}
}
